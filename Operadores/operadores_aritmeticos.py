# Operadores aritmeticos
a = 10
b = 3

# Suma
suma = a + b
print(f'suma: {suma}')

#Resta
resta = a - b
print(f'Resta: {resta}')

#Multiplicacion
multiplicacion = a * b
print(f'Multiplicacion: {multiplicacion}')

#Division (Retorna un tipo float)
division = a / b
print(f'Divisio: {division:.2f}')

#Division entera //
divisionE = a // b
print(f'Division Entera: {divisionE}')

# Modulo
modulo = a % b
print(f'Residuo de la division: {modulo}')

# Exponente
exponente = a ** b
print(f'Potencia: {exponente}')