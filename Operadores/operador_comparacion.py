print(f'***Operadores de Comparacion ***')
a, b = 7, 5
print(f'Valor inicial de a:{a}, b:{b}')

# Operador de igualdad
resultado = a == b
print(f'Es igual a y b?: {resultado}')

# Operador diferente
resultado = a != b
print(f'Es diferente a y b?: {resultado}')

# Operador mayor que
resultado = a > b
print(f'Es mayor a que b?: {resultado}')

# Operador mayor o igual que
resultado = a >= b
print(f'Es mayor o igual que?: {resultado}')

# Operador menor que
resultado = a < b
print(f'Es menor a que b?: {resultado}')

# Operador menor o igual que
resultado = a <= b
print(f'Es menor o igual que?: {resultado}')