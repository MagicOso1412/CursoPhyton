#Variables en phyton

#Declaración e inicialización de variables
edad = 20
altura = 1.75
pais = 'México'

#Acceder a las variables
print("Valores Iniciales")
print('Edad:', edad)
print('Origen', pais)
print('Altura', altura)

#Modificar el valor de una variable (Se sobreescribe)
edad = 30
altura = 1.68

print("Valores Modificados")
print('Edad:', edad)
print('Origen', pais)
print('Altura', altura)

#En phyton el tipo es dinamico (Puede almacenar cualquier tipo de dato, en cualquier momento)
edad = 'Veinte'
print('Edad:', edad)

#Si queremos acceder a una variable no declarada.
telefono = 1412
print("Telefono", telefono)
